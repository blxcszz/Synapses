    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String senha = new String(txtPsw.getPassword());                
        try{
            Administrador adm = new Administrador(email,senha);  
            DAO dao = new DAO();
            Administrador admlogado = dao.existeAdm(adm);
            if(senha.equals(admlogado.getSenha())&& email.equals(admlogado.getEmail())){
                TelaMenuAdm tma = new TelaMenuAdm(admlogado);
                tma.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Email ou senha inv√°lidos");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

